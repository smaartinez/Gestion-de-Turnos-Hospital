    private void configurarRangosYPrecargar() {
    int total = enfSvc.count();

    spIndice.setModel(new javax.swing.SpinnerNumberModel(
        total > 0 ? 1 : 0,
        total > 0 ? 1 : 0,
        total,
        1
    ));

    // Cambiar resumen al mover el índice
    spIndice.addChangeListener(e -> {
        int idx = ((Number) spIndice.getValue()).intValue() - 1;
        cargarResumen(idx);
    });

    if (total > 0) {
        cargarResumen(0);
        btnEliminar.setEnabled(true);
    } else {
        lblResumen.setText("No hay enfermeras.");
        btnEliminar.setEnabled(false);
    }
}

    private void cargarResumen(int idx) {
        if (!enfSvc.indiceValido(idx)) {
            lblResumen.setText("Índice inválido.");
            btnEliminar.setEnabled(false);
            return;
        }
        var e = enfSvc.listar().get(idx); // o getPorIndice(idx)
        String skills = String.join(", ", e.getSkills());
        lblResumen.setText(
            String.format("<html><b>%s</b> (RUT: %s)<br/>Horas máx.: %d | Skills: %s</html>",
                e.getNombre(), e.getRut(), e.getHorasMensualMax(), skills)
        );
        btnEliminar.setEnabled(true);
    }
