    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (enfSvc.count() == 0) return;

        int idx = ((Number) spIndice.getValue()).intValue() - 1;
        if (!enfSvc.indiceValido(idx)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Índice inválido.");
            return;
        }
        var e = enfSvc.listar().get(idx);

        int r = javax.swing.JOptionPane.showConfirmDialog(
            this,
            "¿Eliminar a " + e.getNombre() + " (RUT: " + e.getRut() + ")?",
            "Confirmar eliminación",
            javax.swing.JOptionPane.YES_NO_OPTION
        );
        if (r != javax.swing.JOptionPane.YES_OPTION) return;

        try {
            var eliminada = enfSvc.eliminar(idx); // ✅ usa tu método existente
            javax.swing.JOptionPane.showMessageDialog(
                this,
                "Enfermera eliminada: " + eliminada.getNombre() + " (RUT: " + eliminada.getRut() + ")"
            );
            dispose();
        } catch (IllegalArgumentException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, ex.getMessage(),
                "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

