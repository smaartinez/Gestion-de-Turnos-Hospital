    private void btnGuardarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarEActionPerformed
        // Leer valores de los campos
        String nombre = textnombre.getText().trim();
        String rut    = textRut.getText().trim();
        int horasMax  = (Integer) horasMAX.getValue();
        var skills = Arrays.stream(textskills.getText().split(","))
                           .map(String::trim).filter(s -> !s.isEmpty()).collect(Collectors.toList());

        if (nombre.isEmpty() || rut.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nombre y RUT son obligatorios");
            return;
        }

        enfSvc.agregar(nombre, rut, skills, horasMax);
        JOptionPane.showMessageDialog(this, "Enfermera agregada.");
        dispose();
    }//GEN-LAST:event_btnGuardarEActionPerformed

